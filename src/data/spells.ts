// Get max spell level for a class level
export function getMaxSpellLevel(classLevel: number, castingType: 'full' | 'half' | 'third' | 'pact'): number {
  if (castingType === 'full') {
    return Math.min(9, Math.ceil(classLevel / 2));
  } else if (castingType === 'half') {
    if (classLevel < 2) return 0;
    return Math.min(5, Math.ceil((classLevel - 1) / 4) + 1);
  } else if (castingType === 'third') {
    if (classLevel < 3) return 0;
    return Math.min(4, Math.ceil((classLevel - 2) / 6) + 1);
  } else if (castingType === 'pact') {
    if (classLevel < 1) return 0;
    if (classLevel <= 2) return 1;
    if (classLevel <= 4) return 2;
    if (classLevel <= 6) return 3;
    if (classLevel <= 8) return 4;
    return 5;
  }
  return 0;
}

export function getSpellsByClassAndLevel(className: string, level: number): Spell[] {
  return spells.filter(s => 
    s.classes.some(c => c.toLowerCase() === className.toLowerCase()) && 
    s.level === level
  );
}

