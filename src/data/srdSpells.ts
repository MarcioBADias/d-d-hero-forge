type CastingType = 'full' | 'half' | 'third' | 'pact';

const classCastingTypes: Record<string, CastingType> = {
  bard: 'full',
  cleric: 'full',
  druid: 'full',
  sorcerer: 'full',
  wizard: 'full',
  warlock: 'pact',
  paladin: 'half',
  ranger: 'half',
};

export function getMaxSpellLevel(classLevel: number, classNameOrType: string): number {
  const castingType = classCastingTypes[classNameOrType.toLowerCase()] || classNameOrType as CastingType;
  
  if (castingType === 'full') {
    return Math.min(9, Math.ceil(classLevel / 2));
  } else if (castingType === 'half') {
    if (classLevel < 2) return 0;
    return Math.min(5, Math.ceil((classLevel - 1) / 4) + 1);
  } else if (castingType === 'third') {
    if (classLevel < 3) return 0;
    return Math.min(4, Math.ceil((classLevel - 2) / 6) + 1);
  } else if (castingType === 'pact') {
    if (classLevel < 1) return 0;
    if (classLevel <= 2) return 1;
    if (classLevel <= 4) return 2;
    if (classLevel <= 6) return 3;
    if (classLevel <= 8) return 4;
    return 5;
  }
  return 0;
}

